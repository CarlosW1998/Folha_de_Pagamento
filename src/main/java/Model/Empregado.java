package Model;

public class Empregado {
    private int id;
    private String nome, endereco, tipo;



    public String getNome() {
        return this.nome;
    }

    public String getEndereco() {
        return this.endereco;
    }

    public String getTipo() {
        return this.tipo;
    }

    public int getId() {
        return this.id;
    }
}
